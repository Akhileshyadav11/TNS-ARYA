package basic;

public class DataTypes {

	public static void main(String[] args) 
	{

		// DataTypes:
				/*
				 byte =1 bit for number
				 short =16 bit for number
				 
				 int =32 bit for number
				 long =64 bit for number
				 
				 float =32 bit for float values
				 double =64 bit for float values
				 
				 char =16 bit for character value
				 
				 boolean =8 bit only for true or false
				 */

				short age=23;
				int salary=120000;
				double height=6.4;
				 
				System.out.println(age);
				System.out.println(salary);
				System.out.println(height);
	}

}
